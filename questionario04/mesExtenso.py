mes = int(input()) 

if mes == 1 :
   print("janiero") 

elif mes == 2 : 
    print("fevereiro") 

elif mes == 3 : 
    print("marco") 

elif mes == 4 : 
    print("abril") 

elif mes == 5 : 
    print("maio") 

elif mes == 6 : 
    print("junho") 

elif mes == 7 : 
    print("julho") 

elif mes == 8 : 
    print("agosto") 

elif mes == 9 : 
    print("setembro")
  
elif mes == 10 : 
    print("outubro")
 
elif mes == 11 : 
    print("novembro") 

elif mes == 12 : 
    print("dezembro") 

else : 
    print("invalido")

#Você receberá um número inteiro correspondente ao mês do ano. Considere que o mês de janeiro como 1 e o mês de dezembro como 12. 

#Qualquer inteiro fora desse intervalo deve ser considerado como um mês inválido.

